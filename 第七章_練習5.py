def get_max(x,y,z):
    
    return max(x,y,z)

def get_sum(a,b,c,d):
    
    return (a + b + c + d)

def get_average(a,b,c,d):
    
    return (a + b + c + d)/4


print("最大值:",get_max(10, 1, 54))
print("總和:",get_sum(10, 1, 5,2))
print("平均:",get_average(10, 1, 5,2))

